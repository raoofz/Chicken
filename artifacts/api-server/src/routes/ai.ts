const chatHistory: Map<number, Array<{role: string, content: string}>> = new Map();

router.post("/ai/chat", requireAdmin, async (req: Request, res: Response) => {
  try {
    const { message, newChat } = req.body;
    const userId = req.session.userId!;

    if (!message || typeof message !== "string") {
      res.status(400).json({ error: "الرسالة مطلوبة" });
      return;
    }

    if (newChat) {
      chatHistory.delete(userId);
    }

    let history = chatHistory.get(userId) || [];

    const [flocks, hatchingCycles, tasks, goals, notes] = await Promise.all([
      db.select().from(flocksTable),
      db.select().from(hatchingCyclesTable),
      db.select().from(tasksTable),
      db.select().from(goalsTable),
      db.select().from(dailyNotesTable).orderBy(sql`${dailyNotesTable.date} DESC`).limit(10),
    ]);

    const activeCycles = hatchingCycles.filter(c => c.status === "incubating" || c.status === "hatching");
    const completedCycles = hatchingCycles.filter(c => c.status === "completed" && c.eggsHatched != null);
    const pendingTasks = tasks.filter(t => !t.completed);
    let overallHatchRate = 0;
    if (completedCycles.length > 0) {
      const totalSet = completedCycles.reduce((a, c) => a + c.eggsSet, 0);
      const totalHatched = completedCycles.reduce((a, c) => a + (c.eggsHatched ?? 0), 0);
      overallHatchRate = totalSet > 0 ? Math.round((totalHatched / totalSet) * 100) : 0;
    }

    const farmSummary = `
بيانات مزرعتك الحالية:
- عدد القطعان: ${flocks.length} (إجمالي ${flocks.reduce((a, f) => a + f.count, 0)} طير)
- القطعان: ${flocks.map(f => `${f.name} (${f.breed}, ${f.count} طير, عمر ${f.ageDays} يوم, ${f.purpose})`).join(" | ") || "لا يوجد"}
- دورات التفقيس النشطة: ${activeCycles.length}
- دورات مكتملة: ${completedCycles.length} (معدل فقس: ${overallHatchRate}%)
- الدورات: ${hatchingCycles.map(c => `${c.batchName} (${c.eggsSet} بيضة, حالة: ${c.status}, حرارة: ${c.temperature}°م, رطوبة: ${c.humidity}%)`).join(" | ") || "لا يوجد"}
- المهام المعلقة: ${pendingTasks.length} من ${tasks.length}
- الأهداف: ${goals.map(g => `${g.title} (${g.currentValue}/${g.targetValue} ${g.unit})`).join(" | ") || "لا يوجد"}
- آخر الملاحظات: ${notes.map(n => `[${n.date}] ${n.content}`).join(" | ") || "لا يوجد"}
`;

    const systemPrompt = `أنت "الدكتور نصار" 🐔 — خبير بيطري ومستشار متخصص في تربية الدواجن والتفقيس.
لديك خبرة 30 سنة في إدارة مزارع الدواجن.

${POULTRY_ENCYCLOPEDIA}

---
${farmSummary}
---

## شخصيتك:
- ودود ومحترف، تتحدث بالعربية الفصحى البسيطة
- تستخدم الأرقام والإحصائيات الدقيقة
- تعطي نصائح عملية قابلة للتطبيق فوراً
- تحذر من المخاطر بصراحة
- تربط إجاباتك ببيانات المزرعة الفعلية عندما يكون ذلك مناسباً
- تستخدم إيموجي مناسبة باعتدال

## قواعد:
- أجب بالعربية دائماً
- إذا سُئلت عن شيء خارج مجال الدواجن، أجب بلطف أنك متخصص في الدواجن فقط
- استند للموسوعة أعلاه في إجاباتك
- إذا كانت بيانات المزرعة تشير لمشكلة، نبّه عليها حتى لو لم يُسأل عنها
- رتّب إجاباتك بنقاط واضحة`;

    history.push({ role: "user", content: message });

    if (history.length > 20) {
      history = history.slice(-20);
    }

    const response = await openai.chat.completions.create({
      model: "gpt-4o",
      max_completion_tokens: 8192,
      messages: [
        { role: "system", content: systemPrompt },
        ...history.map(h => ({ role: h.role as "user" | "assistant", content: h.content })),
      ],
    });

    const reply = response.choices[0]?.message?.content;
    if (!reply) {
      res.status(500).json({ error: "لم يتم الحصول على رد" });
      return;
    }

    history.push({ role: "assistant", content: reply });
    chatHistory.set(userId, history);

    res.json({ reply, timestamp: new Date().toISOString() });
  } catch (err: any) {
    logger.error({ err }, "Dr. Nassar chat failed");
    res.status(500).json({ error: "فشل الاتصال: " + (err?.message ?? "خطأ غير معروف") });
  }
});

router.post("/ai/clear", requireAdmin, (_req: Request, res: Response) => {
  chatHistory.delete(_req.session.userId!);
  res.json({ success: true });
});

